package com;

import java.util.List;
import java.util.Map;
import java.util.Set;

public class Student {
	Set id;
	List name;
	Map Address;
	public Set getId() {
		return id;
	}
	public void setId(Set id) {
		this.id = id;
	}
	public List getName() {
		return name;
	}
	public void setName(List name) {
		this.name = name;
	}
	public Map getAddress() {
		return Address;
	}
	public void setAddress(Map address) {
		Address = address;
	}
	
	

}
