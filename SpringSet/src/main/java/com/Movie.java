package com;

import java.util.Set;

public class Movie {
	private Set movieName;
	private Set type;
	private Set language;
	public Set getMovieName() {
		return movieName;
	}
	public void setMovieName(Set movieName) {
		this.movieName = movieName;
	}
	public Set getType() {
		return type;
	}
	public void setType(Set type) {
		this.type = type;
	}
	public Set getLanguage() {
		return language;
	}
	public void setLanguage(Set language) {
		this.language = language;
	}
	
	
	

}
